import streamlit as st

def main():
    st.title("탈모 소개 페이지")
    
    st.header("🔹 탈모증 정의")
    st.markdown("""
    탈모증은 정상적으로 모발이 있어야 할 곳에 모발이 없는 상태를 말합니다.
    모발은 생명 유지에 필수적인 기능을 하지는 않지만, **미용적 역할, 자외선 차단, 머리 보호** 등의 기능을 수행합니다.
    탈모가 심하면 **사회적 활동 및 심리적 영향**이 커질 수 있어, 삶의 질에 중요한 요소입니다.
    """)
    
    st.header("🔹 모발과 모낭")
    st.markdown("""
    - 모발은 **모낭**에서 생성되며, 주기적으로 성장과 휴식을 반복합니다.
    - 머리털 성장 주기: **성장기(2~6년) → 퇴행기(2~4주) → 휴지기(3~4개월)**
    - 모발의 종류: **배냇머리(취모), 솜털(연모), 중간모, 성모**
    - 모발 성장에 영향을 주는 요소: **유전, 남성호르몬(DHT), 성장인자, 환경**
    """)
    
    st.header("🔹 탈모의 종류")
    st.markdown("""
    - **반흔성 탈모:** 모낭이 파괴되어 다시 모발이 자라지 않는 탈모
    - **비반흔성 탈모:** 일반적인 탈모로, 모낭은 존재하지만 모발이 빠지는 형태
    """)
    
    st.header("🔹 대머리 (남성형 탈모)")
    st.markdown("""
    - 유전 + 남성호르몬(DHT) 영향으로 발생하는 탈모
    - 모낭이 시간이 지나면서 점점 축소되는 현상 (**Miniaturization**)
    - 20대 후반 ~ 30대 초반부터 **M자 탈모 & 정수리 탈모**가 진행됨
    """)
    
    st.header("🔹 원형 탈모증")
    st.markdown("""
    - 자각 증상 없이 **갑자기 원형 또는 타원형 형태로 탈모 발생**
    - **면역 질환, 스트레스, 유전적 요인**이 원인일 가능성이 있음
    """)
    
    st.header("🔹 가을철 탈모")
    st.markdown("""
    - 여름철 **강한 자외선, 두피 스트레스**로 인해 가을철에 모발이 빠지는 현상
    - **남성호르몬 증가**로 인해 모발 성장 억제 가능
    - **예방 방법:** 충분한 영양 섭취, 두피 관리, 스트레스 해소
    """)

if __name__ == "__main__":
    main()
