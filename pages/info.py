import streamlit as st

def main():
    st.title("🩺 탈모 정보 및 관리")

    # 자가 진단법
    st.header("🔎 탈모 자가 진단법")
    st.markdown("""
    **1. 모발 가볍게 당기기**  
    - 정상: 1~2개 빠짐  
    - 탈모 의심: 4개 이상 빠짐  

    **2. 하루 탈모량 세기**  
    - 정상: 하루 50~60개 정도 빠짐  
    - 탈모 가능성 있음: 하루 100개 이상 빠짐  

    **3. 모발 관리 상태 확인**  
    - 잦은 염색, 펌, 탈색 여부  
    - 샴푸 후 충분한 헹굼 여부  

    **4. 가족력 확인**  
    - 부모님, 가까운 친척 중 대머리가 있는 경우 탈모 가능성 높음  

    **5. 약물 복용 여부**  
    - 경구 피임약, 비타민 A 과다 섭취 여부  

    **6. 두피 질환 여부**  
    - 비듬, 건선, 지루성 피부염 존재 여부  

    **7. 기타 요소**  
    - 심한 스트레스, 피로, 수면 부족, 급격한 다이어트, 갑상선 질환 등이 있는 경우 탈모 가능성 증가  
    """)

    # 치료 방법
    st.header("💊 탈모 치료 방법")
    st.markdown("""
    **1. 정확한 진단 후 치료 필요**  
    - 탈모 진행 상태에 따라 치료 방법이 다름  

    **2. 식이 조절과 영양 섭취**  
    - 특정 음식이 탈모를 예방하지는 않지만, 균형 잡힌 식단이 중요  
    - 동물성 지방 과다 섭취 줄이기  

    **3. 두피 혈류 개선**  
    - 두피 마사지가 혈류 개선에 도움을 줄 수 있음  
    - 그러나 과도한 마사지나 강한 자극은 오히려 탈모 촉진 가능  

    **4. 약물 치료 (의학적으로 검증된 방법)**  
    - **피나스테리드(Finasteride, 프로페시아)** → 경구 복용  
    - **미녹시딜(Minoxidil)** → 바르는 약  
    - **모발 이식 수술** → 탈모 진행이 심한 경우 고려  
    """)

    # 탈모 예방 및 관리 방법
    st.header("⚠ 탈모 예방 및 주의사항")
